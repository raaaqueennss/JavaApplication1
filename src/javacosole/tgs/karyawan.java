
package javacosole.tgs;
public class karyawan { 
    private final String nama;
    private final double gajiPokok;

    public karyawan( String  nama, double gajiPokok) {
        this.nama = nama;
        this.gajiPokok = gajiPokok;
    }
    public double hitungGaji(){
        return gajiPokok;
    }
    
}
    

